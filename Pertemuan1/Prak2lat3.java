package Pertemuan1;
/**
* Program berikut untuk mencoba operator relasional
*/ 
public class Prak2lat3 {
    public static void main (String args[]){
        int a = 10;
        int b = 20;

        System.out.println("a == b = " + (a == b));
        System.out.println("a != b = " + (a != b));
        System.out.println("a > b = " + (a > b));
        System.out.println("a < b = " + (a < b));
        System.out.println("b >= a = " + (b >= a));
        System.out.println("b == a = " + (b <= a));
    }
}
/** code diatas adalah contoh program penggunaan operator relasional
 *  yang digunakan untuk membandingkan nilai dari dua operand, yang 
 * hasil perbandinganya dinyatakan dalam nilai boolean.
 * nilai boolean true berarti benar dan false berarti salah.
 */